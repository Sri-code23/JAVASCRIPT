/* Arthmetic operators 
Arithmetic operators are used to perform mathematical operations on numbers. Here are the basic arithmetic operators in JavaScript
1. Addition: `a + b`
2. Subtraction: `a - b`
3. Multiplication: `a * b`
4. Division: `a / b`
5. Modulus: `a % b`
6. Exponentiation: `a ** b`
7. Increment: `a++` or `++a`
8. Decrement: `a--` or `--a`
*/

let a = 20;

// addition
a = a + 1;
console.log(a);
// Output: 21

// subtraction
a = a - 3;
console.log(a);
// Output: 18

// multiplication
a = a * 2;
console.log(a);
// Output: 36

//division 
a = a / 2;
console.log(a);
// Output: 18

//Modulus 
a = a % 4;
console.log(a);
// Output: 2

// Increment
a++;
console.log(a);
// Output: 3

// Decrement
a--;
console.log(a);
// Output: 2

// Exponentiation
a = a ** 2;
console.log(a);
// Output: 4


// 
for (initialization; condition; increment) {
  // code to be executed
}

// example
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// for loop with array
let fruits = ['apple', 'banana', 'cherry'];
for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}

// for ...of loop
let numbers = [1, 2, 3, 4, 5];
for(let number of numbers) {
  console.log(number);
}


